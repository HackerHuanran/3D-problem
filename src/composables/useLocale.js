export function useLocale() {
  function t(key, vars = {}) {
    let s = M[lang.value]?.[key] ?? M.zh[key] ?? key
    for (const [k, v] of Object.entries(vars)) s = s.replaceAll(`{${k}}`, v)
    return s
  }
  return { lang, t }
}
